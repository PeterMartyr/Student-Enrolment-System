package PeterMartyr.Library.Models;

/**
 *
 * @author PeterMartyr
 */
public class Node<T> {

        public T value;
        public Node next;
        public Node previous;

        public Node(T value) {
            this.value = value;
        }

        public T getValue() {
            return value;
        }

        public Node getNext() {
            return next;
        }

        public Node getPrevious() {
            return previous;
        }
}
